#!/bin/sh

# Set up.
export PATH=:/bin:/usr/bin:/usr/sbin:/sbin:/usr/local/bin
ArchiveName='Payload.cpio.bz2'
installDir='/usr/local'
TempDir0=`cd /tmp; pwd`
TempDir1='_sb48529-'
Myself=`basename "$0"`
MyPath=`cd \`dirname "$0"\`; pwd`/`basename "$0"`
Postinstall='postinstall'
Preinstall='preinstall'
RezDir='Resources'

# Check arguments.
if [ $# -gt 1 ]; then
	echo 'Error: Wrong number of arguments' 1>&2
	exit 1

# Process argument, if specified.
elif [ $# -eq 1 ]; then
	case "$1" in
	-?|-usage)
		printf 'Usage:\n\t%s {-? | -bom | -pkginfo | -pkgsum | -usage}\n\t%s [dest-dir]\n'\
		 "$Myself" "$Myself"
		exit 1
		;;
	-bom)
		cd $TempDir0 || exit $?
		sed '1,/^##### CUT HERE #####/d' "$MyPath" | base64 --decode | cpio -id $TempDir1/$ArchiveName 2>/dev/null
		bzcat $TempDir1/$ArchiveName | cpio -it 2>/dev/null
		rm -rf $TempDir1
		exit $?
		;;
	-pkgsum|-pkginfo)
		printf '%-16s%s\n%-16s%s\n%-16s%s\n%-16s%s\n' 'Description:' 'CXL library for Linux platforms'\
		 'Author:' "Richard W. Marinelli" 'Version:' '1.1.0' 'Install root:' "$installDir"
		cd $TempDir0 || exit $?
		if [ "$1" = -pkgsum ]; then
			files=$TempDir1/$RezDir/\*
		else
			files=
		fi
		sed '1,/^##### CUT HERE #####/d' "$MyPath" | base64 --decode | cpio -id $files 2>/dev/null
		cd $TempDir1/$RezDir
		for x in "label=Pre name=$Preinstall"\
		 "label=Post name=$Postinstall"; do
			eval $x
			if [ -e $name ]; then
				x=$name
			else
				x='-'
			fi
			printf '%-16s%s\n' "${label}install:" "$x"
		done
		printf 'Resources:\n\n'
		ls -l | sed 1d
		cd ../..
		if [ "$1" = -pkginfo ]; then
			printf '\nPayload:\n\n'
			bzcat $TempDir1/$ArchiveName | cpio -ivt 2>/dev/null
		fi
		rm -rf $TempDir1
		exit $?
		;;
	-*)
		echo "Error: Unknown switch '$1'" 1>&2
		exit 1
		;;
	*)
		installDir=$1
		;;
	esac
fi

[ `id -u` -ne 0 ] && { echo 'Error: You must be root to run this installer' 1>&2; exit 1; }

# Resolve destination directory.
installDir=`cd \`echo $installDir | sed "s,^~,\$HOME,"\` || exit $?; pwd` || exit $?

# Unbundle self.
echo 'Unpacking archive...' 1>&2
cd $TempDir0 || exit $?
sed '1,/^##### CUT HERE #####/d' "$MyPath" | base64 --decode | cpio -imd || exit $?
cd $TempDir1 || exit $?

# Run preinstall script.
if [ -s $RezDir/$Preinstall ]; then
	echo 'Running preinstall script...' 1>&2
	"`pwd`/$RezDir/$Preinstall" "`pwd`" "$installDir" 2>&1 | sed 's/^/    /' 1>&2 || exit $?
fi

# Install payload.
echo 'Installing payload...' 1>&2
cd "$installDir" || exit $?
bunzip2 -c "$TempDir0/$TempDir1/$ArchiveName" | cpio -imd 2>&1 | fgrep -v 'or same age' 1>&2 || exit $?

# Run postinstall script.
cd $TempDir0/$TempDir1
if [ -s $RezDir/$Postinstall ]; then
	echo 'Running postinstall script...' 1>&2
	"`pwd`/$RezDir/$Postinstall" "`pwd`" "$installDir" 2>&1 | sed 's/^/    /' 1>&2 || exit $?
fi

# Clean up and exit.
echo 'Cleaning up...' 1>&2
cd ..
rm -rf $TempDir1
echo 'Done!' 1>&2
exit
